using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Action : Node
{
    protected override void Start()
    {
        base.Start();
    }
    public override Status Tick()
    {
        return base.Tick();
    }

    protected override Status Execute()
    {
        return base.Execute();
    }

    protected override void Exit()
    {
        base.Exit();
    }


}
